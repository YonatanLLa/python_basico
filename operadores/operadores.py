n_uno = 10
n_dos = 20

suma  = n_uno + n_dos
print(f"Numero {n_uno} + {n_dos} = {suma}")

multiplicacion = n_uno * n_dos
print(f"Numero {n_uno} * {n_uno} = {multiplicacion}")

resta = n_uno - n_dos
print(f"Numero {n_uno} - {n_dos} = {resta}")

division, reciduo = divmod(n_uno, n_dos)
print(f"Numero {n_uno} / {n_dos} = {division} y su reciduo, {reciduo}")