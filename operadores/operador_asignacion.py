##El valor en este caso va ir mutando
#Operador igual
x  = 10
print(f'Valor inicial: {x}')

#Incremento
x += 2
print(f'Valor de incremento: {x}')

#Reducir 
x -= 4
print(f'Valor reducido: {x}')

#Multiplicar
x *= 2
print(f'Valor Multiplicado: {x}')

#Dividendo
x //= 4
print(f'valor dividendo: {x}')

#Potencia
x**=3
print(f'Valor potenciado: {x}')

